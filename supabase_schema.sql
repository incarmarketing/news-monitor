-- Supabase schema for the news monitoring database.
-- Run this once in Supabase SQL Editor before enabling GitHub Actions secrets.

create table if not exists public.report_runs (
  id bigint generated by default as identity primary key,
  run_key text not null unique,
  report_date date not null,
  report_slot text not null,
  timestamp timestamptz,
  window_label text,
  window_start timestamptz,
  window_end timestamptz,
  risk_level text default 'LOW',
  metrics jsonb default '{}'::jsonb,
  briefing text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.news_articles (
  id bigint generated by default as identity primary key,
  article_hash text not null unique,
  report_date date not null,
  report_slot text,
  window_label text,
  window_start timestamptz,
  window_end timestamptz,
  risk_level text default 'LOW',
  title text not null,
  link text,
  source text,
  keyword text,
  summary text,
  pub_date timestamptz,
  pub_date_raw text,
  score integer default 0,
  category text default 'other',
  tone text default 'neutral',
  cluster_size integer default 1,
  status text not null default 'new',
  owner text,
  memo text,
  raw jsonb default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.monitor_keywords (
  keyword text,
  category text not null default 'other',
  enabled boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (keyword, category)
);

do $$
begin
  if not exists (
    select 1
      from pg_constraint
     where conname = 'monitor_keywords_category_check'
       and conrelid = 'public.monitor_keywords'::regclass
  ) then
    alter table public.monitor_keywords
      add constraint monitor_keywords_category_check
      check (category in ('own', 'regulation', 'competitor', 'industry', 'other', 'exclude'));
  end if;
end $$;

create table if not exists public.media_relations (
  name text primary key,
  status text default '중립',
  grade text default 'B',
  owner text,
  contact_date date,
  memo text,
  hidden boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.reporters (
  id bigint generated by default as identity primary key,
  name text not null,
  media text not null,
  status text default '중립',
  contact_date date,
  memo text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
