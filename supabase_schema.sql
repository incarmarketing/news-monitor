create table if not exists public.press_aliases (
  host text primary key,
  press_name text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.article_scraps (
  article_hash text primary key,
  article_snapshot jsonb not null default '{}'::jsonb,
  created_by text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.notification_sends (
  id bigint generated by default as identity primary key,
  sent_at timestamptz not null default now(),
  channel text not null default 'kakao',
  message_type text not null,
  dedupe_key text,
  title text,
  body text,
  link_url text,
  status text not null default 'success',
  error text,
  provider_response jsonb default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.negative_watch_runs (
  id bigint generated by default as identity primary key,
  run_key text not null unique,
  scanned_at timestamptz not null default now(),
  minutes_back integer not null default 5,
  scanned_count integer not null default 0,
  negative_count integer not null default 0,
  new_negative_count integer not null default 0,
  status text not null default 'scanned',
  message text,
  created_at timestamptz not null default now()
);

create table if not exists public.job_runs (
  id bigint generated by default as identity primary key,
  run_key text not null unique,
  job_type text not null,
  report_date date,
  report_slot text,
  expected_at timestamptz,
  triggered_by text,
  provider text,
  workflow text,
  github_run_id text,
  github_run_attempt text,
  status text not null default 'started',
  started_at timestamptz,
  finished_at timestamptz,
  last_seen_at timestamptz not null default now(),
  dispatch_count integer not null default 0,
  error text,
  details jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.classification_feedback (
  id bigint generated by default as identity primary key,
  article_hash text,
  title text,
  link text,
  previous_category text,
  previous_tone text,
  corrected_category text,
  corrected_tone text,
  reason text,
  created_by text,
  created_at timestamptz not null default now()
);

alter table public.news_articles add column if not exists summary text;
update public.news_articles
set summary = coalesce(nullif(summary, ''), raw->>'description', raw->>'summary')
where summary is null or summary = '';

